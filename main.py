""" Etch-A-Sketch"""

# Import the Turtle items needed
from turtle import Turtle, Screen

# Creating our Turtle character
tim = Turtle()
screen = Screen()

# Fuctions to move all directions
def move_forwards():
    tim.forward(10)

def move_backwards():
    tim.backward(10)

def turn_left():
    tim.left(10)

def turn_right():
    tim.right(10)

# Clear the screen and start over
def clear():
    tim.clear()
    tim.penup()
    tim.home()
    tim.pendown()

screen.listen()
screen.onkey(move_forwards, "w")
screen.onkey(move_backwards, "s")
screen.onkey(turn_left, "a")
screen.onkey(turn_right, "d")
screen.onkey(clear, "c")
screen.exitonclick()
